package src.com.syntax.Replit;

public class RepTask049Whileloop {

    /*
Write a java program to print "Hello Syntax Team! How are you?" three times using
the while loop
Example Output:
 */

        public static void main(String[] args) {
            int i = 1;
            while (i <= 3) {
                System.out.println("Hello Syntax Team! How are you?");
                i++;
            }
        }
}
